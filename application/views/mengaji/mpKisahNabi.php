            <h3 class="green"><b>Nabi Idris</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Idris adalah keturunan keenam Nabi Adam yang terkenal dengan kecerdasannya.</p>
            <p class="margin-left" style="text-align: justify;">Banyak pesan kebaikan yang disampaikan Nabi Idris, seperti nasihat untuk menjadikan salat jenazah sebagai penghormatan,
                untuk selalu bersyukur, menghindari hasad dan dengki, melarang menumpuk harta yang tak bermanfaat,
                dan mematuhi perintah Allah dengan ikhlas, baik dalam salat, berpuasa, dan amalan lainnya.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Idris memiliki mukjizat sebagai berikut:
            <ol class="margin-left-list" type="a">
                <li>Memiliki kecerdasan mengamati fenomena alam dan mengartikan wahyu Allah.</li>
                <li>Manusia pertama yang bisa membaca dan menulis.</li>
                <li>Manusia pertama yang bisa menjahit pakaian, Nabi Idris bisa menggunakan pakaian yang terindah sementara saat itu orang-orang masih menggunakan kulit binatang.</li>
                <li>Nabi Idris pernah merasakan sakaratul maut dan dihidupkan kembali atas izin Allah.</li>
                <li>Pernah melihat surga dan neraka.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Nuh</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Nuh adalah satu di antara nabi yang masuk golongan Ulul Azmi.</p>
            <p class="margin-left" style="text-align: justify;">Semasa dakwahnya, Nabi Nuh menerima banyak tantangan dan penolakan, termasuk dari anak dan istrinya</p>
            <p class="margin-left" style="text-align: justify;">Ketika kaumnya tetap bertindak zalim, Allah pun membinasakan mereka dalam banjir besar.</p>
            <p class="margin-left" style="text-align: justify;">Namun, Nabi Nuh juga mendapatkan mukjizat, yakni mampu membuat perahu dengan ukuran panjang 300 hasta dan 50 hasta.</p>
            <p class="margin-left" style="text-align: justify;">Setiap makhluk baik manusia maupun binatang yang ada di perahu tersebut selamat dari banjir.</p>
            <br>
            <h3 class="green"><b>Nabi Hud</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Hud diutus Allah bagi kaum 'Ad, kaum yang ahli membuat menara namun senang melakukan kemaksiatan dan menghamburkan harta.</p>
            <p class="margin-left" style="text-align: justify;">Kaum 'Ad terus mengingkari dakwahnya hingga akhirnya Nabi Hud berdoa pada Allah.</p>
            <p class="margin-left" style="text-align: justify;">Allah kemudian menurunkan badai dengan awan yang penuh petir yang membinasakan kaum 'Ad.</p>
            <p class="margin-left" style="text-align: justify;">Mukjizat Nabi Hud antara lain:
            <ol class="margin-left-list" type="a">
                <li>Mampu menurunkan hujan atas izin Allah, kala itu kaum 'Ad dilanda kekeringan hingga tanaman mati dan tak ada sumber air.</li>
                <li>Selamat dari badai petir yang dahsyat.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Shaleh</b></h3>
            <p class="margin-left" style="text-align: justify;">Sekitar kurun waktu 200 tahun, Allah mengutus Nabi Sholeh untuk kaum Tsamud yang masih merupakan keturunan dari kaum 'Ad.</p>
            <p class="margin-left" style="text-align: justify;">Satu di antara mukjizat Nabi Shaleh adalah memunculkan unta betina yang hamil 10 bulan dari batu besar yang terbelah.</p>
            <p class="margin-left" style="text-align: justify;">Kejadian itu membuat sebagian kaum Tsamud menerima dakwah Nabi Shaleh dan menjadi pengikutnya.</p>
            <p class="margin-left" style="text-align: justify;">Namun, sebagian lagi masih ingkar, bahkan membunuh unta yang menjadi mukjizat tersebut.</p>
            <p class="margin-left" style="text-align: justify;">Setelah berhasil membunuh unta, Allah pun membinasakan mereka dengan hantaman petir di malam hari.</p>
            <br>
            <h3 class="green"><b>Nabi Ibrahim</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Ibrahim merupakan satu di antara nabi yang kisahnya banyak diceritakan dalam Al-Qur'an.</p>
            <p class="margin-left" style="text-align: justify;">Mulai bagaimana Nabi Ibrahim menemukan Tuhan, melawan kedzaliman Raja Namrud, hingga kisahnya dengan kedua anaknya yakni Ismail dan Ishaq.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Ibrahim juga memiliki mukjizat antara lain:
            <ol class="margin-left-list" type="a">
                <li>Tetap hidup meski dibakar dengan api.</li>
                <li>Menyaksikan burung dihidupkan kembali.</li>
                <li>Mengubah pasir menjadi makanan.</li>
                <li>Memiliki anak saat usia 99 tahun.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Luth</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Luth merupakan keponakan dari Nabi Ibrahim.</p>
            <p class="margin-left" style="text-align: justify;">Dia diutus Allah untuk berdakwah kepada kaum Sodom, kaum yang rusak dan terkenal dengan perilaku seks sesama jenis.</p>
            <p class="margin-left" style="text-align: justify;">Dakwah Nabi Luth pun ditentang oleh mereka. Sesaat setelah mereka mengusir Nabi Luth, Allah membinasakan mereka dengan bencana gempa bumi dan angin kencang.</p>
            <br>
            <h3 class="green"><b>Nabi Ismail</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Ismail merupakan anak Nabi Ibrahim dan istrinya, Hajar.</p>
            <p class="margin-left" style="text-align: justify;">Satu di antara mukjizat Nabi Ismail masih bisa kita lihat hingga sekarang, yakni air zamzam.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Ismail juga sempat menerima perintah Allah untuk disembelih oleh ayahnya sendiri.</p>
            <p class="margin-left" style="text-align: justify;">Namun, ketika Nabi Ibrahim mengeluarkan benda tajam untuk menyembelihnya, Allah menggantikan Nabi Ismail dengan kambing.</p>
            <p class="margin-left" style="text-align: justify;">Peristiwa itulah yang pada akhirnya menjadi awal mula kurban pada Hari Raya Idul Adha.</p>
            <br>
            <h3 class="green"><b>Nabi Ishaq</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Ishaq merupakan anak dari Nabi Ibrahim dengan Sarah.</p>
            <p class="margin-left" style="text-align: justify;">Dia lahir 14 tahun setelah Nabi Ismail. Dia juga selalu mengikuti ayahnya dalam berdakwah.</p>
            <p class="margin-left" style="text-align: justify;">Dalam Al-Qur'an disebutkan Nabi Ishaq adalah hamba yang memiliki ilmu, akhlak, dan perbuatan yang baik.</p>
            <p class="margin-left" style="text-align: justify;">Dari keturunannya lahir nabi-nabi yang berasal dari Bani Israil.</p>
            <br>
            <h3 class="green"><b>Nabi Yaqub</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Yakub merupakan anak Nabi Ishaq.</p>
            <p class="margin-left" style="text-align: justify;">Dalam beberapa riwayat, Nabi Yaqub digambarkan sebagai sosok yang memiliki karakter kuat dan keimanan luar biasa.
                Bahkan sebelum Nabi Yaqub wafat dan terbaring lemah pun, dia berwasiat kepada para putra untuk tetap menjalankan perintah Allah.</p>
            <br>
            <h3 class="green"><b>Nabi Yusuf</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Yusuf merupakan anak Nabi Yaqub.</p>
            <p class="margin-left" style="text-align: justify;">Dia pernah mendapat mimpi di mana bulan, matahari, dan bintang bersujud padanya.</p>
            <p class="margin-left" style="text-align: justify;">Sejak itulah, Nabi Yaqub mengetahui bahwa putranya akan menjadi orang besar.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Yusuf juga memiliki mukjizat antara lain:
            <ol class="margin-left-list" type="a">
                <li>Memiliki paras yang tampan. Bahkan dalam kisahnya, para wanita pernah mengiris jarinya sendiri saat terpesona dengan ketampanan Nabi Yusuf.</li>
                <li>Mampu menafsirkan mimpi. Nabi Yusuf perah menafsirkan mimpi Raja dan akhirnya diangkat sebagai pejabat di Mesir.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Ayub</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Ayub adalah seorang nabi yang memiliki banyak harta, keturunan, dan berakhlak mulia.</p>
            <p class="margin-left" style="text-align: justify;">Meski begitu, Nabi Ayub tak pernah sombong dan terus berbuat baik.</p>
            <p class="margin-left" style="text-align: justify;">Allah pun menguji Nabi Ayub dengan cobaan yang berat.</p>
            <p class="margin-left" style="text-align: justify;">Dia kehilangan harta, anak-anaknya, dan menderita penyakit kulit.</p>
            <p class="margin-left" style="text-align: justify;">Namun, dalam kondisi tersebut Nabi Ayub tetap bersabar dan berserah diri pada Allah.</p>
            <p class="margin-left" style="text-align: justify;">Beberapa mukjizat Nabi Ayub antara lain:
            <ol class="margin-left-list" type="a">
                <li>Memiliki kesabaran yang luar biasa.</li>
                <li>Mampu mengeluarkan air dari tanah yang bisa menyembuhkan penyakitnya.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Syu'aib</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Syuaib diutus Allah pada kaum Madyan, kaum yang suka menipu dan membangkang.
                Lantaran tetap ingkar dan tak mengikuti Nabi Syuaib, maka Allah memberikan ujian berupa badai panas, mendatangkan awan hitam, hingga gempa bumi yang membinasakan.</p>
            <br>
            <h3 class="green"><b>Nabi Musa</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Musa merupakan Nabi yang memerangi Raja Firaun yang tamak.</p>
            <p class="margin-left" style="text-align: justify;">Dia mempimpin Bani Israil agar menyembah Allah.</p>
            <p class="margin-left" style="text-align: justify;">Atas izin Allah, Nabi Musa juga memiliki mukjizat antara lain:
            <ol class="margin-left-list" type="a">
                <li>Mampu menghidupkan orang mati.</li>
                <li>Memiliki tongkat yang bisa berubah menjadi ular.</li>
                <li>Mampu membelah laut Merah.</li>
                <li>Dianugerahi Kitab Taurat yang berisi tentang 10 firman Allah bagi Bani Israil.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Harun</b></h3>
            <p class="margin-left" style="text-align: justify;">Kisah Nabi Harun sering dikaitkan dengan kisah Nabi Musa.
                Nabi Harun merupakan seorang nabi yang dikaruniai kemampuan bahasa yang sangat baik.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Harun juga berjuang untuk memberantas berhala yang dipimpin oleh Samiri, tukang sihir dari kerajaan milik Firaun.</p>
            <br>
            <h3 class="green"><b>Nabi Dzulkifli</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Dzulkifli merupakan nabi yang tinggal Irak.</p>
            <p class="margin-left" style="text-align: justify;">Tak banyak kisah yang menceritakan Nabi Dzulkifli,
                namun dia dikenal tetap tegar mengajarkan untuk menyembah Allah meski sempat mendapat siksaan, dirantai, hingga dipenjara.</p>
            <br>
            <h3 class="green"><b>Nabi Daud</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Daud merupakan keuturunan Nabi Ibrahim ke-12. Nabi Daud menjadi raja setelah terbunuhnya raja Thalut.</p>
            <p class="margin-left" style="text-align: justify;">Beberapa di antara mukjizat Nabi Daud adalah:
            <ol class="margin-left-list" type="a">
                <li>Dianugerahi kitab Zabur.</li>
                <li>Memiliki suara yang merdu.</li>
            </ol>
            </p>
            <p class="margin-left" style="text-align: justify;">Saat Nabi Daud membaca kitab Zabur, suaranya mampu membuat orang sakit menjadi sembuh,
                membuat air dan angin menjadi tenang, hingga bukit-bukit turut memuji Allah.</p>
            <h3 class="green"><b>Nabi Sulaiman</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Sulaiman adalah satu di antara nabi yang terkenal sebagai raja yang kaya dan memiliki kekuasaan yang besar.</p>
            <p class="margin-left" style="text-align: justify;">Atas izin Allah, Nabi Sulaiman juga memiliki mukjizat antara lain:
            <ol class="margin-left-list" type="a">
                <li>Memiliki kekayaan yang berlimpah. Bahkan dalam suatu riwayat dikisahkan
                    bahwa istana Nabi Sulaiman sangat luas dan bertabur batu mulia.</li>
                <li>Dapat berbicara dengan binatang.</li>
                <li>Mampu menundukkan angin.</li>
                <li>Memiliki bala tentara dari kalangan manusia, jin, dan hewan.</li>
                <li>Mengalirkan tembaga dari perut bumi.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Ilyas</b></h3>
            <p class="margin-left" style="text-align: justify;">Masih di kalangan bani Israel, Nabi Ilyas menghadapi kaum yang gemar menyembah berhala bernama Ba'al.</p>
            <p class="margin-left" style="text-align: justify;">Meski telah menerima dakwah dari Nabi Ilyas,
                mereka tetap ingkar hingga Allah pun menurunkan azab berupa kekeringan yang panjang.</p>
            <br>
            <h3 class="green"><b>Nabi Ilyasa</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Ilyasa adalah anak angkat dari Nabi Ilyas.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Ilyasa diberikan tugas untuk melanjutkan dakwah dari sang ayah untuk Bani Israil.</p>
            <p class="margin-left" style="text-align: justify;">Satu di antara mukjizat Nabi Ilyasa adalah menghidupkan orang mati atas izin Allah.</p>
            <br>
            <h3 class="green"><b>Nabi Yunus</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Yunus adalah nabi yang diutus untuk menyadarkan kaum Assyira penyembah berhala di kota Niniwe.
                Ketika menaiki kapal bersama pengikutnya, awan hitam dan badai besar terjadi. Para penumpang kapal pun mengorbankan Nabi Yunus dan menenggelamkannya ke laut.
                Meski sempat ditelan paus, Allah tetap menyelamatkan Nabi Yunus. Hal inilah yang menjadi satu di antara mukjizat Nabi Yunus.</p>
            <br>
            <h3 class="green"><b>Nabi Dzakaria</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Dzakaria merupakan keturunan Nabi Daud dan Nabi Sulaiman.
                Tak banyak kisah mengenai Nabi Dzakaria, namun menurut beberapa riwayat, Nabi Dzakaria juga memiliki sikap yang rendah hati dan selalu bersyukur kepada Allah.
                Seperti Nabi Ibrahim, Nabi Dzakaria mendapat keturunan saat usianya tak lagi muda.</p>
            </p>
            <br>
            <h3 class="green"><b>Nabi Yunus</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Yahya adalah putra dari Nabi Dzakaria. ia disebut sebagai salah seorang yang benar terhormat dan suci.
                Allah menganugerahi Nabi Yahya dengan kemampuan untuk mengetahui syariat.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Yahya merupakan seorang yang sangat rajin dan gemar membaca.</p>
            <p class="margin-left" style="text-align: justify;">Sejak kecil, Nabi Yahya sudah bisa memutuskan perkara dan mencari jalan keluar untuk segala persoalan.</p>
            <br>
            <h3 class="green"><b>Nabi Isa</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Isa adalah nabi yang tak memiliki ayah dan lahir dari seorang wanita suci bernama Maryam.</p>
            <p class="margin-left" style="text-align: justify;">Beberapa mukjizat Nabi Isa antara lain:
            <ol class="margin-left-list" type="a">
                <li>Bisa berbicara saat bayi.</li>
                <li>Menghidupkan burung dari tanah liat.</li>
                <li>Menghidupkan orang yang sudah mati.</li>
                <li>Menyembuhkan orang buta dan penyakit sopak.</li>
                <li>Menurunkan hidangan dari langit.</li>
                <li>Dianugerahi kitab Injil.</li>
            </ol>
            </p>
            <h3 class="green"><b>Nabi Muhammad</b></h3>
            <p class="margin-left" style="text-align: justify;">Nabi Muhammad merupakan nabi terakhir sekaligus penutup.</p>
            <p class="margin-left" style="text-align: justify;">Tak ada lagi nabi lain setelah Nabi Muhammad wafat. Ketika kelahiran Nabi Muhammad,
                banyak peristiwa besar terjadi, seperti runtuhnya berhala dekat Ka'bah dan padamnya api yang disembah oleh kaum Majusi.</p>
            <p class="margin-left" style="text-align: justify;">Nabi Muhammad SAW memiliki mukzijat, antara lain :
            <ol class="margin-left-list" type="a">
                <li>Mukjizat terbesar adalah Al-Qur'an sebagai kitab suci umat Islam.</li>
                <li>Mampu membelah bulan.</li>
                <li>Mengalirkan air dari jemari tangannya.</li>
                <li>Mampu menurunkan hujan.</li>
                <li>Di atasnya selalu dinaungi awan.</li>
                <li>Melakukan perjalanan ke Sidratul Muntaha saat Isra Miraj, dan masih banyak lagi.</li>
            </ol>
            </p>
